uint16_t csub(uint16_t arg0) {
  int16_t v_0 = ((int16_t)arg0 - 12289);
  return (uint16_t)(v_0 + ((v_0 >> 15) & 12289));
}

uint16_t mulhi(uint16_t x, uint16_t y) {
  uint32_t prod = (uint32_t)x * (uint32_t)y;
  return (uint16_t)(prod >> 16);
}

uint16_t mullo(uint16_t x, uint16_t y) {
  uint32_t prod = (uint32_t)x * (uint32_t)y;
  // return (uint16_t) (prod % (1 << 16));
  uint16_t res = (uint16_t)prod;
  return res;
}

