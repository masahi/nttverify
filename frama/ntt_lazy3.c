uint16_t montgomery_multiply_reduce(uint16_t x, uint16_t y) {
  //@ assert x <= 40957;
  //@ assert y <= 16383;
  uint16_t mlo = mullo(x, y);
  uint16_t mhi = mulhi(x, y);
  uint16_t mlo_qinv = mullo(mlo, 12287);
  uint16_t t = mulhi(mlo_qinv, 12289);
  uint16_t carry = mlo > 0;
  uint16_t res = csub(mhi + t + carry);
  //@ assert res <= 16383;
  return res;
}

